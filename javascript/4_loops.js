// for loop
// starting value; condition; increment amount
for (let i = 1; i <= 10; i++) {
    console.log(i)
}


// while loop
let i = 205
while (i <= 1005) {
    console.log(i)
    i += 200
}


// array forEach method
let nums = [1, 2, 3, 4, 5]
nums.forEach((el, index, arr) => {
    console.log(el)
})
